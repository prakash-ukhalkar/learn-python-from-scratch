# String Operations in Python
# Strings are sequences of characters enclosed in quotes.
# You can perform various operations on strings such as concatenation, repetition, slicing, and methods

# Here are some common string operations:

name = "Python Programming"

print("Upper:", name.upper()) # Convert to uppercase
print("Lower:", name.lower()) # Convert to lowercase
print("Split:", name.split()) # Split into words
print("Slice:", name[0:6])    # Slice 'Python'
print("Replace:", name.replace("Python", "Java")) # Replace substring
print("Length:", len(name)) # Length of the string
print("Find 'Pro':", name.find("Pro"))  # Index of substring
print("Count 'o':", name.count("o"))    # Count occurrences of 'o'




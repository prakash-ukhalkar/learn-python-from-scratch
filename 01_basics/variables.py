# Variables are used to store data values.
# You don't need to declare their type explicitly. 
# Python infers it automatically.
# You can assign a value to a variable using the equals sign (=).
# Variable names should start with a letter or an underscore (_) and can contain letters, numbers, and underscores.
# They are case-sensitive (e.g., 'age' and 'Age' are different variables).
# Avoid using Python reserved keywords as variable names.
# Variable names should be descriptive to make your code more readable.
# Use underscores to separate words in variable names (e.g., my_variable).
# It's a good practice to use lowercase letters for variable names.
# Avoid using single-character variable names except for counters or iterators in loops.
# You can reassign variables to new values at any time.
# Here are some examples of variable assignments:

name = "Alice"       # String variable
age = 30             # Integer variable
height = 5.6         # Float variable
is_student = True    # Boolean variable

print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Is student:", is_student)


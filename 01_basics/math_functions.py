# Demonstrating some common math functions in Python
# The math module provides access to mathematical functions like sqrt, ceil, floor, etc.
# You need to import the math module to use these functions.

# Here are some examples:

import math

print("Pi:", math.pi) # Value of Pi

print("Square root of 16:", math.sqrt(16)) # Square root

print("Ceil of 4.2:", math.ceil(4.2)) # Ceiling value

print("Floor of 4.8:", math.floor(4.8)) # Floor value

print("Power 2^3:", math.pow(2, 3)) # Power

print("Logarithm base 10 of 100:", math.log10(100)) # Logarithm

print("Absolute value of -10:", abs(-10)) # Absolute value

print("Round 4.6:", round(4.6)) # Rounding

print("Max of (3, 7, 2):", max(3, 7, 2)) # Maximum value

print("Min of (3, 7, 2):", min(3, 7, 2)) # Minimum value

print("Sum of (1, 2, 3):", sum([1, 2, 3])) # Sum of elements

print("Factorial of 5:", math.factorial(5)) # Factorial



